bool oled_task_user(void) {
    if (is_keyboard_master()) {
    oled_write_P(PSTR("Layer: "), false);

    switch (get_highest_layer(layer_state)) {
        case _MAIN:
            oled_write_P(PSTR("DVORAK\n"), false);
            break;
        case _NUMB:
            oled_write_P(PSTR("Numbers\n"), false);
            break;
        case _MOVE:
            oled_write_P(PSTR("Movements\n"), false);
            break;
        case _MOBA:
            oled_write_P(PSTR("Moba_Game\n"), false);
            break;
        case _FUNC:
            oled_write_P(PSTR("Functions\n"), false);
            break;
        case _SETT:
            oled_write_P(PSTR("Settings\n"), false);
            break;
        default:
            // Or use the write_ln shortcut over adding '\n' to the end of your string
            oled_write_ln_P(PSTR("Undefined"), false);
    }
    led_t led_state = host_keyboard_led_state();
    oled_write_P(PSTR("WPM:"), false);
    oled_write(get_u8_str(get_current_wpm(), ' '), false);
    oled_write_P(led_state.caps_lock ? PSTR("\nCAPS ") : PSTR("    "), false);
    } else {
        render_space(); // Call this to render the space stuff on the one screen
    }
    return false;
}
